    async def test_multi_session_isolation(self, e2e_plugin):
        """Test that multiple sessions are isolated."""
        sessions = {
            "session-a": [
                ("Tell me about Python", "Python is a programming language."),
                ("What can I do with it?", "You can build web applications."),
            ],
            "session-b": [
                ("Tell me about JavaScript", "JavaScript is a web programming language."),
                ("What can I do with it?", "You can build interactive websites."),
            ]
        }
        
        for session_id, conversation in sessions.items():
            for user_msg, assistant_response in conversation:
                await e2e_plugin.preprocess_message(
                    session_id=session_id,
                    user_message=user_msg
                )
                await e2e_plugin.monitor_response(
                    session_id=session_id,
                    response=assistant_response
                )
        
        # Verify isolation
        metrics_a = await e2e_plugin.get_session_metrics("session-a")
        metrics_b = await e2e_plugin.get_session_metrics("session-b")
        
        assert len(metrics_a) == 2
        assert len(metrics_b) == 2
        
        # Anchors should be different
        anchors_a = await e2e_plugin.get_session_anchors("session-a")
        anchors_b = await e2e_plugin.get_session_anchors("session-b")
        
        # Both should have anchors
        assert len(anchors_a) > 0
        assert len(anchors_b) > 0
    
