"""End-to-end tests simulating real conversations."""

import pytest
from src import BrainGuardPlugin


@pytest.fixture
async def e2e_plugin():
    """Create plugin for E2E tests."""
    plugin = BrainGuardPlugin()
    plugin.config.testing.mock_embeddings = True
    plugin.config.storage.connection_string = ":memory:"
    plugin.config.dashboard.enabled = False
    plugin.config.mode = "adaptive"
    
    await plugin.initialize()
    yield plugin
    await plugin.shutdown()


@pytest.mark.e2e
class TestRealConversations:
    """End-to-end tests simulating real conversations."""
    
    async def test_casual_conversation(self, e2e_plugin):
        """Test casual conversation flow."""
        session_id = "casual-conversation"
        
        conversation = [
            ("Hi there!", "Hello! How can I help you today?"),
            ("What's the weather like?", "I don't have access to real-time weather data."),
            ("Can you tell me a joke?", "Why don't scientists trust atoms? Because they make up everything!"),
            ("That's funny!", "I'm glad you enjoyed it!"),
        ]
        
        for user_msg, assistant_response in conversation:
            await e2e_plugin.preprocess_message(
                session_id=session_id,
                user_message=user_msg
            )
            
            result = await e2e_plugin.monitor_response(
                session_id=session_id,
                response=assistant_response
            )
            
            # Should complete without errors
            assert "metrics" in result
        
        # Check final state
        metrics = await e2e_plugin.get_session_metrics(session_id)
        assert len(metrics) == 4
    
