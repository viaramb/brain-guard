    async def test_repetition_detection(self, e2e_plugin):
        """Test detection of repetitive responses."""
        session_id = "repetition-conversation"
        
        # Same response repeated
        for i in range(5):
            await e2e_plugin.preprocess_message(
                session_id=session_id,
                user_message=f"Question {i}"
            )
            await e2e_plugin.monitor_response(
                session_id=session_id,
                response="This is the same response repeated over and over again."
            )
        
        # Check metrics - variance should be low
        metrics = await e2e_plugin.get_session_metrics(session_id)
        assert len(metrics) == 5
        
        # Later metrics should show low variance
        if len(metrics) >= 3:
            later_metrics = metrics[-3:]
            avg_variance = sum(m["variance"] for m in later_metrics) / len(later_metrics)
            # Variance should be relatively low for repetition
            assert avg_variance < 0.5
    
